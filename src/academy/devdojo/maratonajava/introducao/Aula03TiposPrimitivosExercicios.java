/*
Crie variáveis para os campos descritos abaixo: Eu, [nome], morando no endereço [endereço],
recebi um salário de [salario] na data [data]
 */
public class Aula03TiposPrimitivosExercicios {
    public static void main(String[] args) {
        String Nome = "Marcos André";
        String Endereco = "Avalon Residence";
        float Salario = 2680;
        String DataRecebimentoSalario = "19/12/2023";
        System.out.println(
                "Eu,"+ Nome +", morando no endereço " + Endereco +
                        " recebi um salário de " + Salario + " na data " + DataRecebimentoSalario);
    }
}
